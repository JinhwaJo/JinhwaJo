package package01_var;

public class Example07 {
	public static void main(String[] args) {
		// true 또는 false 를 저장하는 자료형 (타입)
		boolean power = true;
		System.out.println(power);
		
		power = false;
		System.out.println(power);
		
		String a = "abc", b = new String("abc");
		power = (a == b);
		System.out.println(power);
		
		
		
	}
}
