package package01_var;

public class Example05 {
	public static void main(String[] args) {
		// 문자열의 입력 : "" 안에 입력
		String str = "abcde";
		System.out.println(str);
		
		String a = "123";
		System.out.println(a);
		
		String b = "abe_123";
		System.out.println(b);
		
		String c = "한글";
		System.out.println(c);
		
		a = new String("abc");
		b = new String("def");
		c = new String("ghi");
		System.out.println(a + b + c);
		
		
	}
}
